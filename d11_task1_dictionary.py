my_dict = {"name": "Ram", "age": 25, "city": "hyderabad"}

# Accessing dictionary
print(my_dict["name"])

# Modifying dictionary
my_dict["age"] = 28
print(my_dict["age"])

# Adding into dictionary
my_dict["occupation"] = "engineer"
print(my_dict["occupation"])

# Deleting from dictionary
del my_dict["city"]
print(my_dict["city"])
